package com.zvl.model;

public class Item {
  
  String start_time;
  String end_time;
  String order;
  String type;
  String name;
  String description;
  String duration;
  String file_name;
  String file_url;
  String file_path;
  String count_played;
  String thumbnail_url_still;
  String thumbnail_url_animated;
  
  public String getStart_time() {
    return start_time;
  }
  
  public void setStart_time(String start_time) {
    this.start_time = start_time;
  }
  
  public String getEnd_time() {
    return end_time;
  }
  
  public void setEnd_time(String end_time) {
    this.end_time = end_time;
  }
  
  public String getOrder() {
    return order;
  }
  
  public void setOrder(String order) {
    this.order = order;
  }
  
  public String getType() {
    return type;
  }
  
  public void setType(String type) {
    this.type = type;
  }
  
  public String getName() {
    return name;
  }
  
  public void setName(String name) {
    this.name = name;
  }
  
  public String getDescription() {
    return description;
  }
  
  public void setDescription(String description) {
    this.description = description;
  }
  
  public String getDuration() {
    return duration;
  }
  
  public void setDuration(String duration) {
    this.duration = duration;
  }
  
  public String getFile_name() {
    return file_name;
  }
  
  public void setFile_name(String file_name) {
    this.file_name = file_name;
  }
  
  public String getFile_url() {
    return file_url;
  }
  
  public void setFile_url(String file_url) {
    this.file_url = file_url;
  }
  
  public String getFile_path() {
    return file_path;
  }
  
  public void setFile_path(String file_path) {
    this.file_path = file_path;
  }
  
  public String getCount_played() {
    return count_played;
  }
  
  public void setCount_played(String count_played) {
    this.count_played = count_played;
  }
  
  public String getThumbnail_url_still() {
    return thumbnail_url_still;
  }
  
  public void setThumbnail_url_still(String thumbnail_url_still) {
    this.thumbnail_url_still = thumbnail_url_still;
  }
  
  public String getThumbnail_url_animated() {
    return thumbnail_url_animated;
  }
  
  public void setThumbnail_url_animated(String thumbnail_url_animated) {
    this.thumbnail_url_animated = thumbnail_url_animated;
  }
}
